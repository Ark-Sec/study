# We can use logical operators to combine comparisons: *and *or *not
print(1 < 2 < 3)
print(1 < 2 > 3)

print(1 < 2 and 2 < 3)
print(1 < 2 and 2 > 3)

print('h' == 'h' and 2 == 2)
print(('h' == 'h') and (2 == 2))

print((1 == 1) or (50 == 2))

print(not (1 == 1))
print(not (1 == 2))
print(not (400 > 5000))
